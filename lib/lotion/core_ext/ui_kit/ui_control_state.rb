UIControlState = Lotion::Enum.new do |map|
  map[ :normal      ] = UIControlStateNormal
  map[ :highlighted ] = UIControlStateHighlighted
  map[ :disabled    ] = UIControlStateDisabled
  map[ :selected    ] = UIControlStateSelected
  map[ :application ] = UIControlStateApplication
  map[ :reserved    ] = UIControlStateReserved
end
